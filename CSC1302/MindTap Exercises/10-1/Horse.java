public class Horse
{
   String name = "";
   String color = "";
   int birthYear = 0;
   public String getName(){
       return name;
   }
   public void setName(String names){
       name = names;
   }
   public String getColor(){
       return color;
   }
   public void setColor(String colores){
       color = colores;
   }
   public int getBirthYear(){
       return birthYear;
   }
   public void setBirthYear(int year){
       birthYear = year;
   }
}
